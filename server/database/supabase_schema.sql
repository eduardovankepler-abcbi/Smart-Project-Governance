create table if not exists public.recursos (
  id bigint generated by default as identity primary key,
  external_id text default '',
  nome text not null,
  funcao text default '',
  seniority text default '',
  specialties_json text default '[]',
  resource_type text default 'work',
  initials text default '',
  max_units numeric(6,2) default 1,
  standard_rate numeric(12,2) default 0,
  overtime_rate numeric(12,2) default 0,
  email text default ''
);

create table if not exists public.business_units (
  id bigint generated by default as identity primary key,
  nome text not null unique,
  head text default '',
  lider_tec text default '',
  lider_op text default '',
  comercial text default ''
);

create table if not exists public.produtos (
  id bigint generated by default as identity primary key,
  nome text not null,
  business_unit_id bigint not null,
  business_unit_nome text default 'Corporativo'
);

