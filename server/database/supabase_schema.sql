create table if not exists public.tarefas (
  id text primary key,
  parent_id text,
  external_id text default '',
  wbs text default '',
  outline_level integer default 1,
  sort_order integer default 0,
  projeto text not null,
  tarefa text default '',
  subtarefa text default '',
  responsavel text default '',
  funcao text default '',
  data_inicio_planej text default '',
  data_inicio_planej_date date,
  esforco_planej numeric(8,2) default 0,
  data_fim_planej text default '',
  data_fim_planej_date date,
  data_inicio_real text default '',
  data_inicio_real_date date,
  esforco_real numeric(8,2) default 0,
  data_fim_real text default '',
  data_fim_real_date date,
  percentual numeric(5,2) default 0,
  status text default '',
  task_type text default 'fixed_units',
  is_milestone boolean default false,
  duration_minutes integer default 0,
  is_manual boolean default false,
  constraint_type text default '',
  constraint_date text default '',
  constraint_date_date date,
  notes text default '',
  valor_previsto numeric(12,2) default 0,
  valor_gasto numeric(12,2) default 0,
  dias_planejados integer default 0,
  dias_real integer default 0,
  dias_completados integer default 0
);

create table if not exists public.task_assignments (
  id bigint generated by default as identity primary key,
  task_id text not null,
  resource_id bigint,
  resource_name text default '',
  units numeric(8,4) default 1,
  work numeric(10,2) default 0,
  actual_work numeric(10,2) default 0,
  remaining_work numeric(10,2) default 0,
  cost numeric(12,2) default 0
);

create table if not exists public.task_dependencies (
  id bigint generated by default as identity primary key,
  task_id text not null,
  predecessor_task_id text not null,
  dependency_type text default 'FS',
  lag_minutes integer default 0
);

create table if not exists public.comentarios (
  id bigint generated by default as identity primary key,
  entity_type text not null,
  project_id bigint,
  project_name text default '',
  task_id text,
  task_name text default '',
  author_user_id bigint,
  author_nome text default '',
  content text not null,
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);

create table if not exists public.audit_logs (
  id bigint generated by default as identity primary key,
  entity_type text not null,
  entity_id text not null,
  action text not null,
  actor_user_id bigint,
  actor_nome text default '',
  actor_role text default '',
  project_id bigint,
  summary text default '',
  before_json jsonb,
  after_json jsonb,
  created_at timestamptz default now()
);

